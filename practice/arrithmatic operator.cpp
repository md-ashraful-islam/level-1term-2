#include<iostream>

using namespace std;

int main()
{
    int n1,n2;
    cout<<"Enter two number: ";
    cin>>n1>>n2;
    cout<<n1<<" + "<<n2<<" = "<<n1+n2<<endl;
    cout<<n1<<" - "<<n2<<" = "<<n1-n2<<endl;
    cout<<n1<<" * "<<n2<<" = "<<n1*n2<<endl;
    cout<<n1<<" / "<<n2<<" = "<<(float)n1/n2<<endl;

    return 0;
}
